dia = input("Digite dia da semana: ").strip().lower()

if dia == "domingo":
    print("bom fim de semana!")
elif dia == "segunda-feira":
    print("Força nessa semana!")
elif dia == "terça-feira":
    print("Coragem nessa semana!")
elif dia == "quarta-feira":
    print("Aguenta mais um pouco!")
elif dia == "quinta-feira":
    print("Opa, sexta já está perto!")
elif dia == "sexta-feira":
    print("Sextouuu!")
elif dia == "Sábado":
    print("Bora Sabadar!")
else:
    print("Desculpe, mas não é um dia da semana válido.")
